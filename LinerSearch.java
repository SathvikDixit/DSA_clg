public class LinerSearch {
    public static void main(String[] args) {
        int[] arr = {1, 2, 3, 4, 5};
        int n = arr.length;
        for (int i = 0; i < n; i++) {
            if (arr[i] == 4) {
                System.out.println("the element 4 is present in index "+i);
            }
        }
    }
}
